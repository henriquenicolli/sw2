    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        if (!tfId.getText().isEmpty()) {
            ClienteDAO.getInstance().removeById(Integer.parseInt(tfId.getText()));
            clearFields();
            JOptionPane.showMessageDialog(this, "Removido com sucesso !");
        }else{
            JOptionPane.showMessageDialog(this, "ID n√£o informado! ");
            
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (verificaCampos()) {
            Cliente cliente = new Cliente();
            cliente.setCpf(tfCpf.getText());
            cliente.setId(Integer.parseInt(tfId.getText()));
            cliente.setNome(tfNome.getText());
            cliente.setRg(tfRg.getText());
            ClienteDAO.getInstance().merge(cliente);
            clearFields();
            JOptionPane.showMessageDialog(this, "Salvo com sucesso !");
        } else {
            JOptionPane.showMessageDialog(this, "Contem campo vazio ou tipo incorreto de dado!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int id = Integer.parseInt(JOptionPane.showInputDialog("Digite o id do cliente"));
        Cliente cliente = ClienteDAO.getInstance().getById(id);
        tfCpf.setText(cliente.getCpf());
        tfId.setText(String.valueOf(cliente.getId()));
        tfNome.setText(cliente.getNome());
        tfRg.setText(cliente.getRg());
    }//GEN-LAST:event_btnBuscarActionPerformed

